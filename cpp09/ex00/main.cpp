#include "BitcoinExchange.hpp"

int main(int ac, char **av){
	if (ac < 2)
        std::cerr << "Arg Error!" << std::endl;
    else{
        BitcoinExchange btc;
        for (int i=1; i < ac; i++){
            btc._av = av[i];
            btc.FileCheck();
            btc.Exchange();
        }
    }
    return 0;
}